# cores de alienigenas
alien_color = input('Cor do alienigêna: ')
if alien_color == 'verde':
    print('Ganhou 5 pontos por abater o alíenigena!')
elif alien_color == 'amarelo':
    print('Ganhou 10 pontos por abater o alíenigeno!')
elif alien_color == 'vermelho':
    print('Ganhou 15 pontos por abater o alíenigena!')
else:
    print('Alien não abatido!')